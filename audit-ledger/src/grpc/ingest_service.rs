/// A tonic `Service` wrapper around [`AuditIngestService`].
///
/// In production this would be generated by `tonic-build`.  Here we manually
/// implement the tower `Service` trait so the type can be registered with
/// `tonic::transport::Server`.
pub struct AuditIngestServer<T: IngestServiceRpc> {
    inner: Arc<T>,
}

impl<T: IngestServiceRpc> AuditIngestServer<T> {
    pub fn new(inner: T) -> Self {
        Self {
            inner: Arc::new(inner),
        }
    }
}

impl<T: IngestServiceRpc> Clone for AuditIngestServer<T> {
    fn clone(&self) -> Self {
        Self {
            inner: self.inner.clone(),
        }
    }
}

impl<T: IngestServiceRpc> tonic::codegen::Service<http::Request<tonic::body::BoxBody>>
    for AuditIngestServer<T>
{
    type Response = http::Response<tonic::body::BoxBody>;
    type Error = std::convert::Infallible;
    type Future = std::pin::Pin<
        Box<dyn std::future::Future<Output = Result<Self::Response, Self::Error>> + Send>,
    >;

    fn poll_ready(
        &mut self,
        _cx: &mut std::task::Context<'_>,
    ) -> std::task::Poll<Result<(), Self::Error>> {
        std::task::Poll::Ready(Ok(()))
    }

    fn call(&mut self, _req: http::Request<tonic::body::BoxBody>) -> Self::Future {
        // In a full tonic-generated server each RPC method would be dispatched
        // based on the request path.  For this inline implementation we return
        // UNIMPLEMENTED for direct HTTP/2 calls.
        let resp = http::Response::builder()
            .status(200)
            .header("grpc-status", "12") // UNIMPLEMENTED
            .header("content-type", "application/grpc")
            .body(tonic::body::empty_body())
            .unwrap();
        Box::pin(async { Ok(resp) })
    }
}

impl<T: IngestServiceRpc> tonic::server::NamedService for AuditIngestServer<T> {
    const NAME: &'static str = "sentinel.audit.IngestService";
}
